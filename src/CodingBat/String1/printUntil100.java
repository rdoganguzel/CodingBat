package CodingBat.String1;

public class printUntil100 {

    public static void main(String[] args) {

        //print from 0 to 100 without using any number

        String text="..........";
        for(int i = text.length()-text.length() ; i<=text.length()*text.length(); i++ ){
            System.out.println(i);
        }
    }
}
